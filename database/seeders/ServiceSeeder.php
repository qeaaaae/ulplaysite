<?php

declare(strict_types=1);

namespace Database\Seeders;

use App\Models\Category;
use App\Models\Service;
use Illuminate\Database\Seeder;

/**
 * По 10 услуг на каждую корневую категорию (parent_id = null) + три демо-услуги с развёрнутым markdown.
 */
class ServiceSeeder extends Seeder
{
    private const IMAGE = 'https://avatars.mds.yandex.net/get-mpic/5347553/2a00000192cd09d4b4cbb9bb28497c637e4a/optimize';

    private const SERVICES_PER_ROOT_CATEGORY = 10;

    public function run(): void
    {
        $roots = Category::query()
            ->whereNull('parent_id')
            ->orderBy('id')
            ->get();

        if ($roots->isEmpty()) {
            return;
        }

        $n = 0;
        foreach ($roots as $category) {
            for ($i = 1; $i <= self::SERVICES_PER_ROOT_CATEGORY; $i++) {
                $n++;
                $slug = 'service-' . $category->slug . '-' . $i;
                $title = 'Услуга: ' . $category->name . ' — вариант ' . $i;
                $service = Service::updateOrCreate(
                    ['slug' => $slug],
                    [
                        'category_id' => $category->id,
                        'title' => $title,
                        'description' => 'Диагностика, консультация и работы по категории «' . $category->name . '». Срок и стоимость — после осмотра или по фото.',
                        'content' => $i % 2 === 0
                            ? "## Как проходит\n\n1. Оставляете заявку или звоните.\n2. Согласовываем время и объём работ.\n3. Выполняем услугу и выдаём результат.\n\nПодробности — в магазине или по телефону."
                            : null,
                    ]
                );

                $service->images()->delete();
                $service->images()->create([
                    'path' => self::IMAGE,
                    'is_cover' => true,
                    'position' => 0,
                ]);
            }
        }

        $this->seedUlplayFeaturedServices();
    }

    private function seedUlplayFeaturedServices(): void
    {
        $xboxCat = Category::query()->where('slug', 'xbox-series-x')->first()
            ?? Category::query()->where('slug', 'xbox')->first();
        $switchCat = Category::query()->where('slug', 'nintendo-switch')->first()
            ?? Category::query()->where('slug', 'nintendo')->first();
        $dendyCat = Category::query()->where('slug', 'dendy-famiclones')->first()
            ?? Category::query()->where('slug', 'other-consoles')->first();

        if ($xboxCat === null || $switchCat === null || $dendyCat === null) {
            return;
        }

        $featured = [
            [
                'slug' => 'remont-xbox-series-x',
                'category_id' => $xboxCat->id,
                'title' => 'Ремонт Xbox Series X',
                'description' => 'Диагностика бесплатно, замена комплектующих и восстановление платы. Гарантия на работы, сроки по договорённости.',
                'cover' => 'https://xboxflash.ru/wp-content/uploads/2024/01/remont-xbox-series-x-1.jpg',
                'content' => self::markdownXboxSeriesX(),
            ],
            [
                'slug' => 'remont-nintendo-switch',
                'category_id' => $switchCat->id,
                'title' => 'Ремонт и обслуживание Nintendo Switch',
                'description' => 'Батарея, материнская плата, USB-C, дисплей, Joy-Con, чистка и термопаста. Диагностика и консультация.',
                'cover' => 'https://xboxflash.ru/wp-content/uploads/2019/07/Remont-Nintendo-Switch-v-Moskve.jpg',
                'content' => self::markdownNintendoSwitch(),
            ],
            [
                'slug' => 'remont-dendy',
                'category_id' => $dendyCat->id,
                'title' => 'Ремонт игровой приставки Dendy',
                'description' => 'Ремонт Dendy любой модификации: питание, порты джойстиков, видео, слот картриджей. Диагностика бесплатно.',
                'cover' => self::IMAGE,
                'content' => self::markdownDendy(),
            ],
        ];

        foreach ($featured as $row) {
            $service = Service::updateOrCreate(
                ['slug' => $row['slug']],
                [
                    'category_id' => $row['category_id'],
                    'title' => $row['title'],
                    'description' => $row['description'],
                    'content' => $row['content'],
                ]
            );
            $service->images()->delete();
            $service->images()->create([
                'path' => $row['cover'],
                'is_cover' => true,
                'position' => 0,
            ]);
        }
    }

